        private void Timer_Tick(object sender, object e)
        {
            // move up
            if (UpPressed) { 
                dude.MoveUp();
                if (CheckCollision() == true)
                {
                    dude.DudeLocationY += 18;
                }
            }

            // move right
            if (RightPressed) {
                dude.MoveRight();
                if (CheckCollision() == true)
                {
                    dude.DudeLocationX -= 26;
                }
            }

            // move down
            if (DownPressed)
            {
                dude.MoveDown();
                if (CheckCollision() == true)
                {
                    dude.DudeLocationY -= 18;
                }
            }

            // move left
            if (LeftPressed)
            {
                dude.MoveLeft();
                if (CheckCollision() == true)
                {
                    dude.DudeLocationX += 26;
                }
            }
            // rotate butterfly


            // update location
            dude.SetLocation();
            // collision to flower
            //CheckCollision();
        }

        private void CoreWindow_KeyUp(Windows.UI.Core.CoreWindow sender, Windows.UI.Core.KeyEventArgs args)
        {
            switch (args.VirtualKey)
            {
                case VirtualKey.Up:
                    UpPressed = false;
                    break;

                case VirtualKey.Left:
                    LeftPressed = false;
                    break;
                case VirtualKey.Right:
                    RightPressed = false;
                    break;
                case VirtualKey.Down:
                    DownPressed = false;
                    break;
            }
        }

        private void CoreWindow_KeyDown(Windows.UI.Core.CoreWindow sender, Windows.UI.Core.KeyEventArgs args)
        {
            switch (args.VirtualKey)
            {
                case VirtualKey.Up:
                    UpPressed = true;
                    break;

                case VirtualKey.Left:
                    LeftPressed = true;
                    break;
                case VirtualKey.Right:
                    RightPressed = true;
                    break;
                case VirtualKey.Down:
                    DownPressed = true;
                    break;
            }
        }

        private bool CheckCollision()
        {
            foreach (BlockUC wall in blocks)
            {
                if (wall.LocationX == dude.DudeLocationX && wall.LocationY == dude.DudeLocationY) return true;
            }
            if (dude.DudeLocationY < 0 || dude.DudeLocationY >= 900) return true;
            if (dude.DudeLocationX == goal.LocationX && dude.DudeLocationY == goal.LocationY) goalReached();

            return false;
            
        }
