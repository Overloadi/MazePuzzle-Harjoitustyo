        private void CoreWindow_KeyDown(Windows.UI.Core.CoreWindow sender, Windows.UI.Core.KeyEventArgs args)
        {
            switch (args.VirtualKey)
            {
                case VirtualKey.Up:
                    UpPressed = true;
                    break;

                case VirtualKey.Left:
                    LeftPressed = true;
                    break;
                case VirtualKey.Right:
                    RightPressed = true;
                    break;
                case VirtualKey.Down:
                    DownPressed = true;
                    break;
            }
        }

        /// <summary>
        /// Checks if current location has the same coordinates as a wall to prevent player from going through walls
        /// </summary>
        /// <returns>True if player collides with a wall</returns>
        private bool CheckCollision()
        {
            foreach (Wall wall in blocks)
            {
                if (wall.LocationX == dude.DudeLocationX && wall.LocationY == dude.DudeLocationY) return true;
            }
            if (dude.DudeLocationY < 0 || dude.DudeLocationY >= 900) return true;
            if (dude.DudeLocationX == goal.LocationX && dude.DudeLocationY == goal.LocationY) goalReached();

            return false;
            
        }

        /// <summary>
        /// Stop the timers and print a message, when the labyrinth is solved
        /// </summary>
        private async void goalReached()
        {
            timer.Stop();
            stopwatch.Stop();
            double timeElapsed = stopwatch.Elapsed.TotalSeconds;
            stopwatch.Reset();
            WinnerWindow winnerwindow = new WinnerWindow();
            winnerwindow.showTime(timeElapsed);
            await winnerwindow.ShowAsync();
            string name = winnerwindow.getName();
            if (name != "") { 
                HighScore highscore = new HighScore(currentMazeName, name, timeElapsed);
                // highscores.Add(highscore);
                saveHighscores(highscore);
                // highscores.Remove(highscore);
            }
        }

