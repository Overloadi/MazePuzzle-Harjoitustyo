        /// <summary>
        /// When the start button is pressed, start the game. Wall and path are drawn on the canvas and the player character is placed in the maze. Dispatch timer and stopwatch timer are started.
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void StartButton_Click(object sender, RoutedEventArgs e)
        {
            if (currentmatrix == null)
            {
                currentmatrix = matrix10;
            }
            
            blocks = new List<Wall>();
            blocks2 = new List<Path>();
           
            var rowCount = currentmatrix.GetLength(0);
            var colCount = currentmatrix.GetLength(1);
            int sivusuunta = 0;
            int pystysuunta = 0;      

            for (int row = 0; row < rowCount; row++)
            {

                for (int col = 0; col < colCount; col++)
                {
                    if (currentmatrix[row, col] == 0)
                    {
                        Wall wall = new Wall();
                        wall.LocationX = sivusuunta * (MazeCanvas.Width / 50);
                        wall.LocationY = pystysuunta * (MazeCanvas.Height / 50);
                        MazeCanvas.Children.Add(wall);
                        wall.Setlocation();
                        blocks.Add(wall);
                        sivusuunta++;
                    }
                    else if (currentmatrix[row, col] == 1 || currentmatrix[row, col] == 3 || currentmatrix[row, col] == 4)
                    {
                        Path path = new Path();
                        path.LocationX = sivusuunta * (MazeCanvas.Width / 50);
                        path.LocationY = pystysuunta * (MazeCanvas.Height / 50);
                        MazeCanvas.Children.Add(path);
                        path.Setlocation();
                        blocks2.Add(path);
                        sivusuunta++;
                        if (currentmatrix[row, col] == 4)
                        {
                            goal.LocationX = path.LocationX;
                            goal.LocationY = path.LocationY;
                        }
                    }
                }
                pystysuunta++;
                sivusuunta = 0;
            }

            dude = new DudeUC(MazeCanvas.Width / 50 * 4, 0);
            MazeCanvas.Children.Add(dude);

            if (!timer.IsEnabled) {
                timer.Start();
            }
            stopwatch.Start();
        }

        /// <summary>
        /// Listen to keypresses and move accordingly
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void Timer_Tick(object sender, object e)
        {
            // move up
            if (UpPressed) { 
                dude.MoveUp();
                if (CheckCollision() == true)
                {
                    dude.DudeLocationY += 18;
                }
            }

            // move right
            if (RightPressed) {
                dude.MoveRight();
                if (CheckCollision() == true)
                {
                    dude.DudeLocationX -= 26;
                }
            }

            // move down
            if (DownPressed)
            {
                dude.MoveDown();
                if (CheckCollision() == true)
                {
                    dude.DudeLocationY -= 18;
                }
            }

            // move left
            if (LeftPressed)
            {
                dude.MoveLeft();
                if (CheckCollision() == true)
                {
                    dude.DudeLocationX += 26;
                }
            }

            dude.SetLocation();
        }

        /// <summary>
        /// Release the key
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="args"></param>
        private void CoreWindow_KeyUp(Windows.UI.Core.CoreWindow sender, Windows.UI.Core.KeyEventArgs args)
        {
            switch (args.VirtualKey)
            {
                case VirtualKey.Up:
                    UpPressed = false;
                    break;

                case VirtualKey.Left:
                    LeftPressed = false;
                    break;
                case VirtualKey.Right:
                    RightPressed = false;
                    break;
                case VirtualKey.Down:
                    DownPressed = false;
                    break;
            }
        }

